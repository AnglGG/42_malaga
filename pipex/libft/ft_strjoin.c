#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	char	*dest;
	int		aux;

	i = 0;
	j = 0;
	aux = ft_strlen(s1) + ft_strlen(s2) + 1;
	dest = (char *)malloc(aux * sizeof(char));
	if (!dest)
		return (NULL);
	while (s1[i])
		dest[j++] = s1[i++];
	i = 0;
	while (s2[i])
		dest[j++] = s2[i++];
	dest[j] = '\0';
	return (dest);
}

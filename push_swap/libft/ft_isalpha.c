#include "libft.h"

int	ft_isalpha(int c)
{
	int	alpha;

	alpha = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		alpha = 1;
	return (alpha);
}
